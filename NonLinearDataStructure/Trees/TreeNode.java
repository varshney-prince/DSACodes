package NonLinearDataStructure.Trees;

public class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;
    
    

    public TreeNode(int val){
        this.val=val;
        this.left=null;
        this.right=null;
    }



    public TreeNode() {
        this.val=0;
        this.left=null;
        this.right=null;
    }
    
}
